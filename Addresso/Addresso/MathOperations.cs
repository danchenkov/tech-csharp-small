using System;
using System.Collections.Generic;
using System.Linq;
using System.Text;
using System.Threading.Tasks;

namespace Addresso
{
    class MathOperations
    {
        public static int Decrement(int value)
        {
            return value - 1;
        }
        public static int Increment(int value)
        {
            return value + 1;
        }
        public static int Double(int value)
        {
            return value * 2;
        }

    }
}
